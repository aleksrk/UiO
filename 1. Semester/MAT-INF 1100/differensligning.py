from math import sqrt
folge = []
def differensligning(initial0, initial1):
    n = 100
    folge = []
    folge.append(initial0)
    folge.append(initial1)

    for i in range(2, n + 1):
        calc = 4 * folge[i - 1] + folge[i - 2]
        folge.append(calc)

    return folge


